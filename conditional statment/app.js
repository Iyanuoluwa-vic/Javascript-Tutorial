// conditional statment
const num1 = 10;
const num2 = 20;
if (num2 > num1) {
  console.log(num2 + " is greater than " + num1);
} else if (num2 < num1) {
  console.log(num2 + " is not greater than " + num1);
} else {
  console.log("it is equal");
}

// equality operator
const num3 = 10;
const num4 = 20;

if (num3 == num4) {
  console.log("it is equal");
} else {
  console.log("it is not equal");
}
